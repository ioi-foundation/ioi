function toolcatProbe(stageMarker, toolName, options = {}) {
  return {
    kind: `${stageMarker.toLowerCase().replace(/_/g, " ")} ${toolName}`,
    executionMode: "agent",
    prompt: [
      stageMarker,
      "TOOLCAT_SINGLE_TOOL",
      `toolcat_tool=${toolName}`,
      "Run exactly this live IDE Rust/provider tool row through Agent mode, then preserve the concrete trace verdict.",
      ...TOOLCAT_SINGLE_TOOL_MARKERS,
      options.instructions || "",
    ].filter(Boolean).join(" "),
    assistantVisibleTimeoutMs: options.assistantVisibleTimeoutMs,
    isolateThread: options.isolateThread ?? true,
  };
}

function toolcatProbes(stageMarker, toolNames, options = {}) {
  return toolNames.map((toolName) => toolcatProbe(stageMarker, toolName, options));
}

